places = ["Vancouver", "Shanghai", "Seattle", "Victory", "Calgary"]
print(places)

print("sorted")
print(sorted(places))
print(places)

print("sorted - reverse")
print(sorted(places, reverse=True))
print(places)

print("reverse")
print(places.reverse())
print(places)

print("reverse again")
places.reverse()
print(places)

print("sort")
places.sort()
print(places)

print("sort: reverse")
places.sort(reverse=True)
print(places)
